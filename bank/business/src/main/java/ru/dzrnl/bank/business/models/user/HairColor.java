package ru.dzrnl.bank.business.models.user;

/**
 * Represents possible hair colors for a user.
 */
public enum HairColor {

    /**
     * Blonde hair color.
     */
    BLONDE,

    /**
     * Brown hair color.
     */
    BROWN,

    /**
     * Black hair color.
     */
    BLACK,

    /**
     * Other hair color, used when the user's hair color doesn't fall under predefined categories.
     */
    OTHER
}
